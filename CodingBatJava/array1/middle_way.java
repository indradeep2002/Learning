public class middle_way {
    public static int[] middleWay(int[] a, int[] b) {
        int ans[] = {a[1] , b[1]};
        
        return ans;
    }

    public static void main(String[] arguments){

        int a[] = {1, 2, 3};
        int b[] = {4, 5, 6};

        System.out.println(middleWay(a, b));
    }
      
}
